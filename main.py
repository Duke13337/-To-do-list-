# Список для хранения задач
tasks = []

def add_task():
    task = input("Введите название задачи: ")
    tasks.append(task)
    print(f"Задача '{task}' успешно добавлена!")

# Основной цикл программы
if __name__ == "__main__":
    print("--- Программа: Список задач ---")
    add_task()  # Вызываем функцию добавления
    print("Ваш список задач:", tasks)
